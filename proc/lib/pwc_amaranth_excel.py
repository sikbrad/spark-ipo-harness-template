"""ACC3030 거래처계정내역 → ERP-style Excel formatter.

Reproduces the column layout, grouping, subtotals, and 거래처 합계 / 총계
rows that the ERP UI's 'Excel 변환하기' export produces. Use this so the
file generated by the playwright-cli pipeline is byte-for-byte (or close to
it) interchangeable with a human-exported workbook.
"""

from __future__ import annotations

from collections import OrderedDict
from pathlib import Path

from openpyxl import Workbook
from openpyxl.styles import Font, PatternFill, Alignment


HEADER_36 = [
    '거래처코드', '거래처명', '회계단위코드', '회계단위명',
    '계정과목코드', '계정과목명', '승인일', '승인번호', '적요',
    '차변', '대변', '잔액', None, '전표',
    '사용부서코드', '사용부서명', '프로젝트코드', '프로젝트명',
    '관리번호', '발생일', '만기일', '요청일', '상환일',
    '환율', '외화', '이자율', '세무구분',
    '사용자정의코드', '사용자정의명', '사용자정의코드', '사용자정의명',
    '계좌번호', '예금주', '금융기관명', '작성사원코드', '작성사원명',
]


def _carry_first(r: dict) -> int:
    return 0 if '이 월' in (r.get('rmkDc') or '') else 1


def _sort_key(r: dict):
    return (
        r.get('trCd') or '',
        r.get('acctCd') or '',
        _carry_first(r),
        str(r.get('fillDt') or ''),
        str(r.get('fillNb') or ''),
        r.get('dispSq') or r.get('lnSq') or 0,
    )


def _fmt_date(yyyymmdd) -> str | None:
    s = str(yyyymmdd or '')
    if len(s) == 8 and s.isdigit():
        return f'{s[:4]}-{s[4:6]}-{s[6:]}'
    return None


def _fmt_ctnb(fillDt, fillNb, seq) -> str | None:
    if not (fillDt and fillNb is not None and seq is not None):
        return None
    try:
        seq_i = int(seq)
    except (TypeError, ValueError):
        return None
    if seq_i <= 0:
        return None
    fd = str(fillDt)
    fn = str(fillNb)
    if len(fd) != 8 or not fd.isdigit():
        return None
    return f'{fd}-{fn:>05}-{seq_i:05d}'


def _new_row() -> list:
    return [None] * 36


def _norm(v):
    """Normalize empty strings to None; keep numbers/strings."""
    return None if v == '' else v


def _data_row(r: dict, first_in_group: bool, running_balance: float, attrNm: str, divCd, divNm) -> list:
    row = _new_row()
    if first_in_group:
        row[0] = r.get('trCd')
        row[1] = attrNm
        row[2] = divCd
        row[3] = divNm
    row[4] = r.get('acctCd')
    row[5] = r.get('acctNm')
    row[6] = _fmt_date(r.get('fillDt'))
    row[7] = r.get('fillNb')
    row[8] = r.get('rmkDc')
    row[9] = r.get('drAm') or 0
    row[10] = r.get('crAm') or 0
    # idx 11 (잔액 header) stays None for data rows; idx 12 holds running balance
    row[12] = running_balance
    row[13] = _fmt_ctnb(r.get('fillDt'), r.get('fillNb'),
                        r.get('dispSq') if r.get('dispSq') is not None else r.get('lnSq'))
    row[14] = r.get('deptCd')
    row[15] = r.get('deptNm')
    row[16] = r.get('pjtCd')
    row[17] = r.get('pjtNm')
    # 18 (관리번호) / 19 (발생일) / 21 (요청일) / 22 (상환일): API doesn't expose
    row[20] = _norm(r.get('toDt'))            # 만기일
    row[23] = r.get('ctQt') or None           # 환율 (KRW=1, foreign=actual rate)
    row[24] = r.get('ctAm') or None           # 외화 (foreign currency amount)
    row[25] = 0                               # 이자율: manual fills 0 default
    # 26~30: 세무구분 / 사용자정의: API doesn't expose
    row[31] = r.get('baNb')
    row[32] = r.get('depositor')
    row[33] = r.get('bankNm')
    row[34] = r.get('empCd')
    row[35] = r.get('korNm')
    return row


def _subtotal_row(acct_nm: str, dr_sum: float, cr_sum: float) -> list:
    row = _new_row()
    row[5] = f'{acct_nm} 계'
    row[9] = dr_sum
    row[10] = cr_sum
    row[12] = 0
    return row


def _customer_total_row(name: str, dr_sum: float, cr_sum: float) -> list:
    row = _new_row()
    row[5] = f'{name} 합계'
    row[9] = dr_sum
    row[10] = cr_sum
    net = dr_sum - cr_sum
    row[11] = '차' if net >= 0 else '대'
    row[12] = abs(net)
    return row


def _grand_total_row(dr_sum: float, cr_sum: float) -> list:
    row = [''] * 36
    row[0] = '총계'
    row[9] = dr_sum
    row[10] = cr_sum
    net = dr_sum - cr_sum
    row[11] = '차' if net >= 0 else '대'
    row[12] = abs(net)
    return row


