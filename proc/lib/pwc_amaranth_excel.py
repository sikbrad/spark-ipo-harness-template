ACC3010_HEADER = [
    '날짜', '적요', '차변', '대변', '잔액',
    '거래처코드', '거래처명', '거래처분류코드', '거래처분류명',
    '적요구분', '계좌번호', '금융기관', '예금주',
    '작성일', '작성번호', '분개순번', '승인번호',
    '회계단위코드', '회계단위명', '부서코드', '부서명',
    '사원코드', '사원명', '계정과목코드', '계정과목명',
    '프로젝트코드', '프로젝트명', '사용부서코드', '사용부서명',
    '관리사원코드', '관리사원명',
]


def _acc3010_row(r: dict, running_balance: float, grp: str = '1') -> list:
    """Build one ACC3010 data row (31 columns)."""
    fd = _fmt_date(r.get('fillDt'))
    return [
        fd,                              # 0 날짜
        r.get('rmkDc'),                  # 1 적요
        r.get('acctAmDr') or 0,          # 2 차변
        r.get('acctAmCr') or 0,          # 3 대변
        running_balance,                  # 4 잔액
        r.get('trCd'),                   # 5 거래처코드
        r.get('trNm'),                   # 6 거래처명
        r.get('trgrpCd'),                # 7 거래처분류코드
        r.get('trgrpNm'),                # 8 거래처분류명
        grp,                             # 9 적요구분 (0/1/2/3)
        r.get('baNb'),                   # 10 계좌번호
        r.get('bankNm'),                 # 11 금융기관
        r.get('depositor'),              # 12 예금주
        str(r.get('fillDt') or '') or None,  # 13 작성일 (raw YYYYMMDD)
        r.get('fillNb'),                 # 14 작성번호
        r.get('lnSq') or r.get('dispSq'),  # 15 분개순번
        r.get('fillNb'),                 # 16 승인번호 (= 작성번호 same)
        r.get('divCd'),                  # 17 회계단위코드
        r.get('divNm'),                  # 18 회계단위명
        r.get('deptCd'),                 # 19 부서코드
        r.get('deptNm'),                 # 20 부서명
        r.get('empCd'),                  # 21 사원코드
        r.get('empNm'),                  # 22 사원명
        r.get('acctCd'),                 # 23 계정과목코드
        r.get('acctNm'),                 # 24 계정과목명
        r.get('pjtCode'),                # 25 프로젝트코드
        r.get('pjtName'),                # 26 프로젝트명
        r.get('ctDept'),                 # 27 사용부서코드
        r.get('ctDeptNm'),               # 28 사용부서명
        r.get('mgmtEmpCd'),              # 29 관리사원코드
        r.get('mgmtEmpNm'),              # 30 관리사원명
    ]


def _acc3010_marker_row(rmk: str, dr: float, cr: float, balance: float,
                         tr: dict, grp: str) -> list:
    """Build a [전월이월] / [월계] / [누계] marker row."""
    row = [None] * 31
    row[1] = rmk           # 적요
    row[2] = dr            # 차변
    row[3] = cr            # 대변
    row[4] = balance       # 잔액
    row[5] = tr.get('trCd')
    row[6] = tr.get('trNm')
    row[7] = tr.get('trgrpCd')
    row[8] = tr.get('trgrpNm')
    row[9] = grp
    return row


def build_acc3010_xlsx(api_rows: list[dict], path: str | Path) -> tuple[int, dict]:
    """Build ACC3010 거래처원장 xlsx — transaction-level with running balance,
    [월계] per month, [누계] per accumulator, mirroring ERP UI Export.

    Input api_rows: output of acc3010_period_view() — each row is either
    a carry (rmkDc='[ 전 월 이 월 ]', grp='0') or a real transaction
    (grp='1' with fillDt set).
    """
    from collections import OrderedDict

    # Group rows by (trCd, acctCd), then within group by (year-month) for
    # 월계 / 누계 markers.
    grouped: OrderedDict = OrderedDict()
    for r in api_rows:
        tr = r.get('trCd')
        if not tr:
            continue
        grouped.setdefault(tr, []).append(r)

    out_rows: list[list] = [ACC3010_HEADER]
    for tr_cd, rows in grouped.items():
        # Sort: carry first, then transactions by fillDt+lnSq
        carry_rows = [r for r in rows if '이 월' in (r.get('rmkDc') or '')]
        tx_rows = [r for r in rows if '이 월' not in (r.get('rmkDc') or '')]
        tx_rows.sort(key=lambda r: (
            str(r.get('fillDt') or ''),
            str(r.get('fillNb') or ''),
            r.get('lnSq') or r.get('dispSq') or 0,
        ))

        sample = (carry_rows or tx_rows)[0] if (carry_rows or tx_rows) else {}
        running = 0.0
        cum_dr = cum_cr = 0.0  # cumulative since start of carry
        month_dr = month_cr = 0.0
        cur_month: Optional[str] = None

        # Emit carry first (no month marker before it)
        for cr_row in carry_rows:
            dr = cr_row.get('acctAmDr') or 0
            cr = cr_row.get('acctAmCr') or 0
            running += dr - cr
            cum_dr += dr
            cum_cr += cr
            out_rows.append(_acc3010_row(cr_row, running, grp='0'))

        # Walk transactions; emit 월계+누계 at month change
        for tx in tx_rows:
            fd = str(tx.get('fillDt') or '')
            ym = fd[:6] if len(fd) >= 6 else cur_month
            if cur_month and ym != cur_month and (month_dr or month_cr):
                # End previous month
                out_rows.append(_acc3010_marker_row(
                    '[ 월     계 ]', month_dr, month_cr, running, sample, '2'))
                out_rows.append(_acc3010_marker_row(
                    '[ 누     계 ]', cum_dr, cum_cr, running, sample, '3'))
                month_dr = month_cr = 0.0
            cur_month = ym
            dr = tx.get('acctAmDr') or 0
            cr = tx.get('acctAmCr') or 0
            running += dr - cr
            cum_dr += dr
            cum_cr += cr
            month_dr += dr
            month_cr += cr
            out_rows.append(_acc3010_row(tx, running, grp='1'))

        # Tail markers for last month / 누계
        if month_dr or month_cr or carry_rows or tx_rows:
            out_rows.append(_acc3010_marker_row(
                '[ 월     계 ]', month_dr, month_cr, running, sample, '2'))
            out_rows.append(_acc3010_marker_row(
                '[ 누     계 ]', cum_dr, cum_cr, running, sample, '3'))

    wb = Workbook()
    ws = wb.active
    ws.title = '거래처원장'
    for r in out_rows:
        ws.append(r)
    for c in ws[1]:
        c.font = Font(bold=True, color='FFFFFF')
        c.fill = PatternFill('solid', fgColor='305496')
        c.alignment = Alignment(horizontal='center', vertical='center')
    for col in ws.columns:
        w = max((len(str(c.value or '')) for c in col[:200]), default=10)
        ws.column_dimensions[col[0].column_letter].width = min(max(w + 2, 10), 40)
    ws.freeze_panes = 'A2'
    wb.save(path)

    return len(out_rows), {
        'data_rows': len(out_rows) - 1,
        '거래처': len(grouped),
    }


def build_acc3030_xlsx(api_rows: list[dict], path: str | Path) -> tuple[int, dict]:
    """Build ERP-styled ACC3030 xlsx from raw API rows. Returns (row_count, summary)."""
    rows = sorted(api_rows, key=_sort_key)

    # Group: trCd → acctCd → [rows] (preserve insertion order)
    groups: OrderedDict[str, OrderedDict[str, list]] = OrderedDict()
    for r in rows:
        tr, acct = r.get('trCd'), r.get('acctCd')
        if not (tr and acct):
            continue
        groups.setdefault(tr, OrderedDict()).setdefault(acct, []).append(r)

    out_rows: list[list] = [HEADER_36]
    grand_dr = grand_cr = 0.0
    for tr, accts in groups.items():
        first_row = next(iter(accts.values()))[0]
        attrNm = first_row.get('attrNm') or ''
        divCd = first_row.get('divCd') or ''
        divNm = first_row.get('divNm') or ''

        tr_dr = tr_cr = 0.0
        for acct, acct_rows in accts.items():
            acctNm = acct_rows[0].get('acctNm') or ''
            running = 0.0
            acct_dr = acct_cr = 0.0
            for i, r in enumerate(acct_rows):
                dr = r.get('drAm') or 0
                cr = r.get('crAm') or 0
                running += dr - cr
                out_rows.append(_data_row(r, first_in_group=(i == 0),
                                          running_balance=running,
                                          attrNm=attrNm, divCd=divCd, divNm=divNm))
                acct_dr += dr
                acct_cr += cr
            out_rows.append(_subtotal_row(acctNm, acct_dr, acct_cr))
            tr_dr += acct_dr
            tr_cr += acct_cr

        out_rows.append(_customer_total_row(attrNm, tr_dr, tr_cr))
        grand_dr += tr_dr
        grand_cr += tr_cr

    out_rows.append(_grand_total_row(grand_dr, grand_cr))

    # Write workbook
    wb = Workbook()
    ws = wb.active
    ws.title = '거래처계정내역'
    for r in out_rows:
        ws.append(r)
    for c in ws[1]:
        if c.value:
            c.font = Font(bold=True, color='FFFFFF')
            c.fill = PatternFill('solid', fgColor='305496')
            c.alignment = Alignment(horizontal='center', vertical='center')
    for col in ws.columns:
        w = max((len(str(c.value or '')) for c in col[:200]), default=10)
        ws.column_dimensions[col[0].column_letter].width = min(max(w + 2, 10), 40)
    ws.freeze_panes = 'A2'
    wb.save(path)

    return len(out_rows), {
        'data_rows': len(out_rows) - 1,
        'grand_dr': grand_dr,
        'grand_cr': grand_cr,
        'net_balance': grand_dr - grand_cr,
        '거래처': len(groups),
    }
