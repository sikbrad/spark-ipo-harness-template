def markdown_table_to_docx(doc: Document, table_lines: list[str]) -> None:
    rows: list[list[str]] = []
    for line in table_lines:
        cells = [clean(cell) for cell in line.strip().strip("|").split("|")]
        if cells and all(re.fullmatch(r":?-{3,}:?", cell or "") for cell in cells):
            continue
        rows.append(cells)
    if not rows:
        return
    width = max(len(row) for row in rows)
    table = doc.add_table(rows=1, cols=width)
    table.style = "Table Grid"
    for idx, value in enumerate(rows[0]):
        table.rows[0].cells[idx].text = value
    for row in rows[1:]:
        cells = table.add_row().cells
        for idx in range(width):
            cells[idx].text = row[idx] if idx < len(row) else ""
    doc.add_paragraph()


def set_korean_font(run, font_name: str = "Malgun Gothic") -> None:
    run.font.name = font_name
    run.font.size = Pt(9.5)
    rpr = run._element.rPr
    rfonts = rpr.rFonts
    if rfonts is None:
        rfonts = OxmlElement("w:rFonts")
        rpr.append(rfonts)
    rfonts.set(qn("w:eastAsia"), font_name)


def add_paragraph(doc: Document, text: str, style: str | None = None) -> None:
    para = doc.add_paragraph(style=style)
    run = para.add_run(text)
    set_korean_font(run)


def markdown_to_docx(markdown: str, path: Path) -> None:
    doc = Document()
    styles = doc.styles
    styles["Normal"].font.name = "Malgun Gothic"
    styles["Normal"].font.size = Pt(9.5)
    for section in doc.sections:
        section.top_margin = Pt(54)
        section.bottom_margin = Pt(54)
        section.left_margin = Pt(54)
        section.right_margin = Pt(54)

    lines = markdown.splitlines()
    idx = 0
    while idx < len(lines):
        line = lines[idx].rstrip()
        if not line:
            idx += 1
            continue
        if line.startswith("|"):
            table_lines = []
            while idx < len(lines) and lines[idx].startswith("|"):
                table_lines.append(lines[idx])
                idx += 1
            markdown_table_to_docx(doc, table_lines)
            continue
        if line.startswith("# "):
            add_paragraph(doc, line[2:].strip(), "Title")
        elif line.startswith("## "):
            add_paragraph(doc, line[3:].strip(), "Heading 1")
        elif line.startswith("### "):
            add_paragraph(doc, line[4:].strip(), "Heading 2")
        elif line.startswith("- "):
            add_paragraph(doc, line[2:].strip(), "List Bullet")
        elif re.match(r"^\d+\. ", line):
            add_paragraph(doc, re.sub(r"^\d+\. ", "", line), "List Number")
        else:
            add_paragraph(doc, line)
        idx += 1

    doc.add_paragraph().add_run().add_break(WD_BREAK.LINE)
    doc.save(path)


def build_markdown(person: str, summary: dict) -> str:
    return "\n".join(
        [
            f"# {person} 업무규칙 SOP",
            "",
            evidence_note(person, summary),
            "",
            BODY_MD[person].strip(),
            "",
        ]
    )


def main() -> None:
    summary = load_json(SUMMARY_PATH, {})
    OUT_MD.mkdir(parents=True, exist_ok=True)
    OUT_DOCX.mkdir(parents=True, exist_ok=True)
    outputs = []

    for person in TARGETS:
        markdown = build_markdown(person, summary[person])
        md_path = OUT_MD / f"{person}_업무규칙_SOP_{RUN_DATE}.md"
        docx_path = OUT_DOCX / f"{person}_업무규칙_SOP_{RUN_DATE}.docx"
        md_path.write_text(markdown, encoding="utf-8")
        markdown_to_docx(markdown, docx_path)
        with zipfile.ZipFile(docx_path) as zf:
            assert "[Content_Types].xml" in zf.namelist()
        outputs.append((person, md_path, docx_path))

    print("created")
    for person, md_path, docx_path in outputs:
        print(f"{person}\t{md_path}\t{docx_path}\t{docx_path.stat().st_size}")


if __name__ == "__main__":
    main()
