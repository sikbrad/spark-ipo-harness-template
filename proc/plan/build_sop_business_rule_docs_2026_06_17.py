            "- 원천 데이터: `data/teams/full-history/2026-06-17`, `output/teams-business-rules/2026-06-17/person_evidence`, `data/teams/peoples/*/업무규칙`",
            f"- 부서/직책: {dept} / {position}",
            f"- ERP main_work: {main_work}",
            f"- 근거 메시지: {summary.get('evidence_count')}건 (직접 발화 {authored_count(person, summary)}건 + 관련 채널/언급 근거 포함)",
